using System;

namespace CalculatorApp
{
	public class Calculator
	{
		public double Add(double a, double b)
		{
			return a + b;
		}

		public double Subtract(double a, double b)
		{
			return a - b;
		}

		public double Multiple(double a, double b)
		{
			return a * b;
		}

		public double Divied(double a, double b)
		{
			return a / b;
		}
	}
}